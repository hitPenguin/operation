// linux的基本命令

`touch 文件名` // 新建文件
`rm 文件名` // 删除文件
`mkdir 文件夹` // 新建文件夹
`rm -r 文件夹` // 删除文件夹和目录下的文件
`cat 文件` // 查看一个文件


// vi 编辑器
`command mode` | `insert mode` | `last line mode`
`command mode 按 i 进入 insert mode， 按 : 进入 last line mode`
`insert mode 按 ESC 进入 command mode`

`last line mode ： 
  :w filename 将文章以指定的文件名filename保存
  :wq 存盘并退出
  :q! 不存盘强制退出`
